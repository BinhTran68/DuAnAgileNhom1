    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // Bước 1. Lấy đối tượng sinh viên mới
       SinhVien sinhVien = readForm();
       int kq = sinhVienService.insertSinhVien(sinhVien);
       if (kq > 0) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
           try {
               fillTable(sinhVienService.getAllSinhViens());
           } catch (Exception ex) {
               Logger.getLogger(SinhVienView.class.getName()).log(Level.SEVERE, null, ex);
           }
        }else {
           JOptionPane.showConfirmDialog(this, "Thêm thành công");
       }
        
        
        

    }//GEN-LAST:event_btnThemActionPerformed

    SinhVien readForm() {  
        String ma,ten,nganh;
        double diem;
        int gioiTinh;
        
        ma = txMaSv.getText().trim();
        ten = txtHoTen.getText().trim();
        nganh = cboNganh.getSelectedItem().toString();
        diem = Double.parseDouble(txtDiemTB.getText());
        
        if (rdNam.isSelected()) {
            gioiTinh = 1;
        }else if (rdNu.isSelected()) {
            gioiTinh = 0;
        }else {
            gioiTinh = 2;
        }
        
        return new SinhVien(ma, ten, diem, nganh, gioiTinh);
        
    }
    
