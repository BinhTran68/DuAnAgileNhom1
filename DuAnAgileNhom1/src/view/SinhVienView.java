    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa");
            return;
        }
        
        int row = tblSinhVien.getSelectedRow();

//        System.out.println(row);

        String ma = defaultTableModel.getValueAt(row, 1).toString();
        
        int kq = sinhVienService.delete(ma);
        
        if (kq > 0) {
            JOptionPane.showMessageDialog(this, "Xóa thành công ");
            this.fillTable(sinhVienService.getAllSinhViens());
        }else {
             JOptionPane.showMessageDialog(this, "Xóa không thành công ");
        }
       
        
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnTimKiemTheoTenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemTheoTenMouseClicked
       
        String ten =  JOptionPane.showInputDialog(this, "Vui lòng nhập tên cần tìm");
        
       
        List<SinhVien> kq = sinhVienService.findSinhVienByTen(ten);
        if (kq.size() == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thây kết quả nào");
            fillTable(sinhVienService.getAllSinhViens());
        } else {
            JOptionPane.showMessageDialog(this, "Tìm được "+kq.size() + " sinh viên trùng khớp");
            fillTable(kq);
        }
        
        
    }//GEN-LAST:event_btnTimKiemTheoTenMouseClicked

